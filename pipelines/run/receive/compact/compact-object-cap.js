// @loader: tenx

import { TenXObject, TenXEnv, TenXLookup, TenXConsole, TenXDate, TenXString, TenXMath, TenXLog } from '@tenx/tenx'

// Per-container compaction predicate.
//
// Reads a per-container cap-file that declares, for each container,
// whether to compact via encode() or preserve fullText. Containers not
// listed in the file fall back to `compactReceiverDefault`. The container
// identity is the value of `compactReceiverContainerField` (defaults to
// the k8s container name).
//
// The env-var that gates this module remains `compactReceiverLookupFile`
// (the engine's `TenXReceiver.encodeField()` helper checks that name to
// decide whether to emit `encoded=shouldEncode() ? encode() : fullText`
// in the forwarder output stream). The file content shifted from
// per-pattern to per-container, but the env-var contract is unchanged.
//
// Entry format (CSV with `container,value` header):
//
//     container,value
//     <container>,<true|false>[:<untilEpochSec>][:<reason>]
//
// `true`           -> compact via encode()
// `false`          -> preserve fullText (explicit per-container opt-out)
// `untilEpochSec`  -> optional TTL; past it the entry self-heals to a no-op
//                     and the container falls back to compactReceiverDefault
// `reason`         -> optional free-text for audit; must not contain commas
//
// Hot-reloaded on in-place file writes (the gitops pattern). Do NOT source
// this file from a Kubernetes ConfigMap mount: CM updates swap the file
// via a symlink rename and the engine's stat-based watcher will not see
// the change.

export class CompactInput extends TenXInput {

    static shouldLoad(config) {
        return TenXEnv.get("compactReceiverLookupFile");
    }

    constructor() {

        if (!TenXEnv.get("quiet")) {
            TenXConsole.log("🗜️ Loading compact cap file: " + TenXEnv.get("compactReceiverLookupFile"));
        }

        var lastModified = TenXLookup.load(TenXEnv.get("compactReceiverLookupFile"), true);
        var retain = TenXEnv.get("compactReceiverLookupRetain", 300000);

        if (TenXDate.now() - lastModified > retain) {
            if (!TenXEnv.get("quiet")) {
                TenXConsole.log("⚠️ compact receiver cap file is stale, lastModified: {}, retainInterval: {}",
                    lastModified, retain);
            }
            TenXLog.info("compact receiver cap file is stale, lastModified: {}, retainInterval: {}",
                lastModified, retain);
        }
    }
}

export class CompactObject extends TenXObject {

    static shouldLoad(config) {
        return TenXEnv.get("compactReceiverLookupFile");
    }

    // Per-event compact decision. Pure -- returns the bool, doesn't mutate
    // the event. Called by the forwarder output stream's field expression
    // `encoded=shouldEncode() ? encode() : fullText` (generated by the
    // engine's TenXReceiver.encodeField() helper at config-load time when
    // compactReceiverLookupFile is set).
    get shouldEncode() {

        if ((!this.isObject) || (this.isDropped)) return false;

        var defaultEncodeRaw = TenXEnv.get("compactReceiverDefault", false);
        var defaultEncode = (defaultEncodeRaw == true) || (defaultEncodeRaw == "true");

        // Inline the env lookup; a local var passed to this.get() is treated as an event field.
        var container = this.get(TenXEnv.get("compactReceiverContainerField"));
        if (!container) return defaultEncode;

        var entry = TenXLookup.get("compactReceiverLookupFile", container);
        if (!entry) return defaultEncode;

        // Entry shape: `<true|false>[:<untilEpochSec>][:<reason>]`. Parse the
        // value and the optional TTL the same way rate-object-cap.js parses
        // its `<bytes>[:<untilEpochSec>][:<reason>]` entries.
        var c1 = TenXString.indexOf(entry, ":", 0);
        var valueEnd = (c1 < 0) ? TenXString.length(entry) : c1;
        var entryValue = TenXString.substring(entry, 0, valueEnd);

        if (c1 >= 0) {
            var c2 = TenXString.indexOf(entry, ":", c1 + 1);
            var untilEnd = (c2 < 0) ? TenXString.length(entry) : c2;
            var untilEpochSec = TenXMath.parseDouble(TenXString.substring(entry, c1 + 1, untilEnd));
            if (untilEpochSec > 0 && (TenXDate.now() / 1000) > untilEpochSec) {
                // TTL expired -- fall back to the default like the entry was never there.
                return defaultEncode;
            }
        }

        return TenXString.startsWith(entryValue, "true");
    }
}
